{
    var input = document.getElementById("input").value;
    sessionStorage.setItem("input",input);
}
function myFun()            //main function for searching elements
{
    var txt,products,val;
    products = document.getElementsByClassName("card");     //Here this vari. stores objects of HTML node elements which have class "Cards"
    input = sessionStorage.getItem("input");                //Get the value of input from input field from other page
    document.getElementById("input").value = input;         //Set value of search page as input in session storage
    for(let i=0;i<products.length;i++)
    {
        products[i].style.display = "none";                 //set 'display' property of all cards on search page as "none" 
    }
    var count1=0;       //This is used to set properties of cards(which have display as "none")
    for (let count = 0; count < prod.length; count++)
    {
        val = prod[count].name;             //Get name of products from the array

        txt = val.toUpperCase();            //for comparing, convert all letters to capital

        if(txt.indexOf(input.toUpperCase()) > -1)   //here indexOf function matches the value "input" element if match found then returns index of that word else returns -1
        {

            if(input.length == 0)       //If input field is empty all cards become invisible and appropriate message is displayed 
            {
                for(let i=0;i<products.length;i++)
                {
                    products[i].style.display = "none";
                }
                break;
            }

            if(count1 == products.length)       //I have set only six cards so if the limit is reached break the loop
            {
                break;
            }
                //Set the empty cards with the values from array of objects. The End:)
            products[count1].querySelector("#name").innerHTML = prod[count].name;
            products[count1].querySelector("#ads").href = prod[count].href;
            products[count1].querySelector("#ima").src = prod[count].link;
            products[count1].querySelector("#value").innerHTML = prod[count].price;
            products[count1].style.display = "flex";
            count1++;
        }
    }
}